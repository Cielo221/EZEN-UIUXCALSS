//Math (수학 객체)

let num = 2.2234;
let maxNum = Math.max(10, 5, 8, 30);
console.log(maxNum);

let minNum = Math.min(10, 5, 8, 30);
console.log(minNum);

let roundNum = Math.round(num);
console.log(roundNum);

let floorNum = Math.floor(num);
console.log(floorNum);

let ceilNum = Math.ceil(num);
console.log(ceilNum);

let rudNum = Math.random();
console.log(rudNum);

let piNum = Math.PI;
console.log(piNum);
